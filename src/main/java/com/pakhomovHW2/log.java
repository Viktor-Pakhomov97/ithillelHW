package com.pakhomovHW2;

public class log {
    public static void main(String[] args) {
        double a, b;
        a = 2;
        b = 8;
        System.out.println(Math.log10(a));
        System.out.println(Math.log10(b));
        System.out.println("Result: " + Math.log10(b) / Math.log10(a));
    }
}
